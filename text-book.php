<script>
    async function searchBooks() {
        const query = document.getElementById("searchInput").value;
        const resultsContainer = document.getElementById("results");
        const iframeContainer = document.getElementById("iframeContainer");
        const iframe = document.getElementById("bookPreviewIframe");

        resultsContainer.innerHTML = "";
        iframeContainer.style.display = "none";
        iframe.src = "";

        if (!query) {
            resultsContainer.innerHTML = "<p>Please enter a search term.</p>";
            return;
        }

        const apiUrl = `https://www.googleapis.com/books/v1/volumes?q=${query}&maxResults=5`;

        try {
            const response = await fetch(apiUrl);
            const data = await response.json();

            if (data.items && data.items.length > 0) {
                data.items.forEach(book => {
                    const title = book.volumeInfo.title || "No title available";
                    const authors = book.volumeInfo.authors ? book.volumeInfo.authors.join(", ") : "Unknown author";
                    const previewLink = book.volumeInfo.previewLink || "";

                    const bookElement = document.createElement("div");
                    bookElement.classList.add("book");
                    bookElement.onclick = function() {
                        if (previewLink) {
                            iframe.src = previewLink;
                            iframeContainer.style.display = "block";
                        } else {
                            alert("No preview available for this book.");
                        }
                    };

                    bookElement.innerHTML = `
                        <p class="book-title">${title}</p>
                        <p class="book-author">by ${authors}</p>
                    `;

                    resultsContainer.appendChild(bookElement);
                });
            } else {
                resultsContainer.innerHTML = "<p>No books found.</p>";
            }
        } catch (error) {
            console.error("Error fetching books:", error);
            resultsContainer.innerHTML = "<p>There was an error fetching books. Please try again later.</p>";
        }
    }
</script>
